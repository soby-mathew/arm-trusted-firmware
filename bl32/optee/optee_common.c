#include <stdint.h>
#include <types.h>
#include "optee.h"

/*
 * Override the default implementation in Trusted Firmware. OP-TEE device
 * physical addresses aren't identity mapped and hence we make use OP-TEE
 * helpers to translate the physical address back to virtual ones.
 */
uintptr_t plat_phys_to_virt(unsigned long long pa)
{
	uintptr_t pa_32 = (uintptr_t)pa;
	return (uintptr_t)phys_to_virt(pa_32, MEM_AREA_IO_SEC);
}
